package com.bbj.firstkotlinprogram

fun main(args: Array<String>) {

    var a:Boolean = true
    var b:Char = 'R'
    var c:Byte = -12            // [-128,+127] or [-2^8,+2^8-1]
    var d:Short = -356          // [-32768, +32767] or [-2^16,+2^16-1]
    var e:Int = 43543           // 32 bit
    var f:Long = -432543234L    // 64 bit
    var g:Float = 3.256724F
    var h:Double = 7.423423454534

    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)
}